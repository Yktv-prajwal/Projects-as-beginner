a=int(input("Enter Your Marks:"))

if  a>=90 and a==100:
    print("You Got A+ Grade For The Marks :",a)
elif a>=80 and a<=89:
    print("You Got A Grade For The Marks :",a)
elif a>=70 and a<=79:
    print("You Got B+ Grade For The Marks :",a)
elif a>=60 and a<=69:
    print("You Got B Grade For The Marks :",a)
elif a>=50 and a<=59:
    print("You Got C+ Grade For The Marks :",a)
elif a>=40 and a<=49:
    print("You Got C Grade For The Marks :",a)

else:
    print("You Are Fail By Obtaining Marks:",a)

